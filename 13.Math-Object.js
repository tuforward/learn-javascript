/**             Math Object
 * YÊU CẦU:
        - Tìm hiểu thêm một số hàm toán học sau:
        - round() => Làm tròn lên hoặc xuống.
            + phần thập phân nhỏ hơn 5 thì làm tròn xuống

        - ceil() => Làm tròn lên.
        
        - floor() => Làm tròn xuống.
        
        - min() => Lấy số nhỏ nhất.
        
        - max() => Lấy số lớn nhất.
        
        - pow() => Lũy thừa.
        
        - random() => Tạo số thập phân ngẫu nhiên từ 0 đến 1. (có thập phân)
        
        - trunc() => Xóa số thập phân.

    + Phỏng đoán kết quả của console.log().
 */

console.log(Math.round(99.2)); 

console.log(Math.round(99.5));

console.log(Math.ceil(99.2));

console.log(Math.floor(99.9));

console.log(Math.min(10, 20, 100, -100, 90));

console.log(Math.max(10, 20, 100, -100, 90));

console.log(Math.pow(2, 4));

console.log(Math.random());

console.log(Math.trunc(99.5));