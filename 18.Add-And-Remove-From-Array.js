//  VÍ DỤ 1:
const myFriends_Examp_01 = ["Le Van A", "Nguyen Thi B", "Do Van C", "Dao Thi D"];

myFriends_Examp_01.unshift("Vu Van E", "Nguyen Van F");
console.log(myFriends_Examp_01);

// VÍ DỤ 2:
const myFriends_Examp_02 = ["Le Van A", "Nguyen Thi B", "Do Van C", "Dao Thi D"];

myFriends_Examp_02.push("Vu Van E", "Nguyen Van F");
console.log(myFriends_Examp_02);

// VÍ DỤ 3:
const myFriends_Examp_03 = ["Le Van A", "Nguyen Thi B", "Do Van C", "Dao Thi D"];

myFriends_Examp_03.shift();
