// equal operator
console.log(2 == 2);
console.log(2 == '2');

console.log('\n');

// not equal operator
console.log(3 != 2);
console.log('hello' != 'Hello'); 

console.log('\n');

// strict equal operator
console.log(2 === 2);
console.log(2 === '2');

console.log('\n');

// strict not equal operator
console.log(2 !== '2');
console.log(2 !== 2);